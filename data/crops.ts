export const getCropsByCategory = (category: 'vegetable' | 'fruit' | 'flower' | 'herb'): Crop[] => {
  return allCrops.filter(crop => crop.category === category);
};

export const searchCrops = (query: string): Crop[] => {
  const lowerQuery = query.toLowerCase();
  return allCrops.filter(crop => 
    crop.name.toLowerCase().includes(lowerQuery) ||
    (crop.scientificName && crop.scientificName.toLowerCase().includes(lowerQuery))
  );
};
