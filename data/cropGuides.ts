// Helper function to normalize crop names to keys
export function normalizeCropKey(cropName: string): string {
  return cropName
    .toLowerCase()
    .replace(/\s+/g, '_')
    .replace(/-/g, '_')
    .replace(/[^a-z0-9_]/g, '');
}

// Helper function to get crop guide with fallback
export function getCropGuide(cropName: string): CropGuide | null {
  const key = normalizeCropKey(cropName);
  return cropGuides[key] || null;
}
